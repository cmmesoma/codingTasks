# Python programming exercise
# the aim is to convert one variable to another

num1 = 99.23
new_num1 = int(num1) # float variable is converted to integer.
print(new_num1) # prints out 99.

num2 = 23
new_num2 = float(num2) # integer variable converted to float
print(new_num2) # prints out 23.0

num3 = 150
new_num3 = str(num3) # integer variable to string.
print(new_num3) # prints out 150
print(type(new_num3)) # confirmation of variable class

string1 = "100"
new_string = int(string1) # string to integer
print(new_string) # prints out 100

